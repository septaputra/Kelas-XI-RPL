package com.example.recycleviewcardview;

public class Siswa {
    private String nama;
    private String alamat;

    public Siswa(String nama, String alamat) {
        this.nama = nama;
        this.alamat = alamat;
    }

    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }
}
